//! # MEMBRA Governance Program
//!
//! A production-quality multisig governance and treasury authorization program
//! for the MEMBRA Money Protocol.
//!
//! ## Core invariant
//! No treasury action can be taken without going through the full
//! **propose → approve → timelock → execute** flow.  There is no backdoor; no
//! admin can bypass governance.
//!
//! ## Architecture
//! This program is a **pure authorization layer**.  It does not move funds or
//! modify parameters in other programs directly.  Instead:
//!
//! 1. A registered signer creates a `Proposal` describing the desired action.
//! 2. Signers add approvals until the threshold is met, transitioning the
//!    proposal to `Approved` and starting the timelock clock.
//! 3. After the timelock elapses (and before the execution window closes) any
//!    registered signer calls `execute_approved_action`, which marks the
//!    proposal `Executed`.
//! 4. The **target program** (IDO, Rebase, Rewards, …) reads the `Proposal`
//!    PDA on-chain, verifies its status is `Executed`, and only then permits
//!    the privileged operation.
//!
//! This design keeps cross-program invocation (CPI) complexity out of the
//! governance layer while still enforcing the governance invariant on-chain.

